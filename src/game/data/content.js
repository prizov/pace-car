function catmullRom(p0, p1, p2, p3, t) {
  const t2 = t * t;
  const t3 = t2 * t;
  return 0.5 * (
    (2 * p1) +
    (-p0 + p2) * t +
    (2 * p0 - 5 * p1 + 4 * p2 - p3) * t2 +
    (-p0 + 3 * p1 - 3 * p2 + p3) * t3
  );
}

function buildSmoothWaypoints(anchors) {
  const points = [];
  const count = anchors.length;

  for (let i = 0; i < count; i++) {
    const p0 = anchors[(i - 1 + count) % count];
    const p1 = anchors[i];
    const p2 = anchors[(i + 1) % count];
    const p3 = anchors[(i + 2) % count];
    const dist = Math.hypot(p2.x - p1.x, p2.y - p1.y);
    const steps = Math.max(4, Math.round(dist / 55));

    for (let step = 0; step < steps; step++) {
      const t = step / steps;
      const x = Math.round(catmullRom(p0.x, p1.x, p2.x, p3.x, t));
      const y = Math.round(catmullRom(p0.y, p1.y, p2.y, p3.y, t));
      const s = Math.max(0.38, Math.min(1, catmullRom(p0.s, p1.s, p2.s, p3.s, t)));
      const prev = points[points.length - 1];
      if (prev && Math.hypot(prev.x - x, prev.y - y) < 8) continue;
      points.push({ x, y, s: Number(s.toFixed(2)) });
    }
  }

  return points;
}

